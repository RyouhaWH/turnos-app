// Función para obtener paleta de colores para shifts/index
export const getRoleColors = (role: any) => {
    const hexColor = role.color || '#3B82F6';
    const palette = ROLE_COLOR_PALETTES[hexColor] || ROLE_COLOR_PALETTES['#3B82F6'];

    // Determinar icono basado en el nombre del rol
    const getIcon = (roleName: string) => {
        const lowerName = roleName.toLowerCase();
        if (lowerName.includes('proximidad') || lowerName.includes('alerta')) return 'Car';
        if (lowerName.includes('fiscalización') || lowerName.includes('fiscalizacion')) return 'UserCheck2';
        if (lowerName.includes('ciclo') || lowerName.includes('bicicleta')) return 'Bike';
        if (lowerName.includes('motorizado')) return 'Bike';
        if (lowerName.includes('dron') || lowerName.includes('drone')) return 'Plane';
        if (lowerName.includes('despacho') || lowerName.includes('coordinador')) return 'Radio';
        return 'Users';
    };

    return {
        ...palette,
        icon: getIcon(role.nombre)
    };
};

// Función para obtener clases CSS para dashboard
export const getDashboardRoleColors = (hexColor: string): string => {
    const palette = ROLE_COLOR_PALETTES[hexColor] || ROLE_COLOR_PALETTES['#3B82F6'];
    return `${palette.dashboardBg} ${palette.dashboardText} ${palette.dashboardBorder} ${palette.dashboardDarkBg} ${palette.dashboardDarkText} ${palette.dashboardDarkBorder}`;
};

